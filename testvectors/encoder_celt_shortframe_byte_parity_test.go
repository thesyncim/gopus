// celtShortFrameCases is the 2.5ms/5ms portion of encoderComplianceSummaryCases
// that this file gates with byte-parity assertions.
type celtShortFrameCase struct {
	name      string
	frameSize int // samples at 48 kHz
	channels  int
	bitrate   int
}

func celtShortFrameByteParityCases() []celtShortFrameCase {
	return []celtShortFrameCase{
		{name: "CELT-FB-2.5ms-mono-64k", frameSize: 120, channels: 1, bitrate: 64000},
		{name: "CELT-FB-2.5ms-stereo-128k", frameSize: 120, channels: 2, bitrate: 128000},
		{name: "CELT-FB-5ms-mono-64k", frameSize: 240, channels: 1, bitrate: 64000},
		{name: "CELT-FB-5ms-stereo-128k", frameSize: 240, channels: 2, bitrate: 128000},
	}
}

// TestEncoderCELTShortFrameVariantByteParityAgainstLibopusFixture asserts that
// gopus CELT 2.5 ms and 5 ms FB packets are byte-identical to the pinned libopus
// 1.6.1 variant fixture across all four signal variants.
//
// amd64: hard gate (zero diffs required).
// arm64: FMA residual documented per cell above; CI (amd64) is the authoritative gate.
func TestEncoderCELTShortFrameVariantByteParityAgainstLibopusFixture(t *testing.T) {
	t.Parallel()
	requireTestTier(t, testTierParity)
	requireStrictLibopusReference(t)

	isArm64 := runtime.GOARCH == "arm64"
	variants := testsignal.EncoderSignalVariants()
	cases := celtShortFrameByteParityCases()

	// One subtest per case × variant pair — mirrors cbrTestMatrix structure.
	for _, tc := range cases {
		for _, variant := range variants {
			tc, variant := tc, variant
			name := fmt.Sprintf("%s-%s", tc.name, variant)
			t.Run(name, func(t *testing.T) {
				t.Parallel()

				// Find the fixture case for this configuration + variant.
				fixtureCase, ok := findEncoderVariantsFixtureCase(
					encoder.ModeCELT,
					types.BandwidthFullband,
					tc.frameSize,
					tc.channels,
					tc.bitrate,
					variant,
				)
				if !ok {
					t.Fatalf("variant fixture case not found for %s[%s]", tc.name, variant)
				}

				totalSamples := fixtureCase.SignalFrames * fixtureCase.FrameSize * fixtureCase.Channels
				signal, err := testsignal.GenerateEncoderSignalVariant(variant, 48000, totalSamples, tc.channels)
				if err != nil {
					t.Fatalf("generate signal: %v", err)
				}
				if hash := testsignal.HashFloat32LE(signal); hash != fixtureCase.SignalSHA256 {
					t.Fatalf("signal hash mismatch: got=%s want=%s", hash, fixtureCase.SignalSHA256)
				}

				libPackets, _, err := decodeEncoderVariantsFixturePackets(fixtureCase)
				if err != nil {
					t.Fatalf("decode fixture packets: %v", err)
				}
				goPackets, err := encodeGopusForVariantsCase(fixtureCase, signal)
				if err != nil {
					t.Fatalf("encode gopus packets: %v", err)
				}
				if len(goPackets) != len(libPackets) {
					t.Fatalf("packet count mismatch: got=%d want=%d", len(goPackets), len(libPackets))
				}

				var diffFrames []int
				for i := range libPackets {
					if !bytes.Equal(goPackets[i], libPackets[i]) {
						diffFrames = append(diffFrames, i)
					}
				}

				diffRate := float64(len(diffFrames)) / float64(len(libPackets))
				t.Logf("byte diffs: %d/%d (%.2f%%)", len(diffFrames), len(libPackets), 100*diffRate)

				if len(diffFrames) == 0 {
					return
				}

				// Report first few diffs for diagnosis (max 3).
				for i, fi := range diffFrames {
					if i >= 3 {
						t.Logf("  ... and %d more differing frames", len(diffFrames)-3)
						break
					}
					got, want := goPackets[fi], libPackets[fi]
					limit := min(len(want), len(got))
					firstDiff := limit
					for b := 0; b < limit; b++ {
						if got[b] != want[b] {
							firstDiff = b
							break
						}
					}
					t.Logf("  frame %d len(got=%d want=%d) firstByteDiff=%d", fi, len(got), len(want), firstDiff)
				}

				if !encoderCELTFloatBoundaryBuild() {
					// amd64 asm/SIMD build: this fixture was generated by the SIMD
					// libopus, so it is the matched-tier reference here — hard gate.
					t.Fatalf("CELT short-frame byte parity FAIL: %d/%d packets differ (arch=%s/%s)",
						len(diffFrames), len(libPackets), runtime.GOOS, runtime.GOARCH)
				}

				if isArm64 {
					// arm64 (pure-Go float): soft gate against the per-cell FMA
					// residual budget measured vs this SIMD fixture.
					key := fmt.Sprintf("%s|%s", tc.name, variant)
					maxRate, ok := celtShortFrameByteParityArm64MaxResidualRate[key]
					if !ok {
						maxRate = 0.10 // conservative default
					}
					if diffRate > maxRate {
						t.Fatalf("arm64 FMA residual exceeded budget: %.2f%% > %.2f%% for %s[%s]; "+
							"root cause: CELT float FMA contraction vs clang -ffp-contract=on "+
							"(project_arm64_celt_1ulp_drift.md)",
							100*diffRate, 100*maxRate, tc.name, variant)
					}
					t.Logf("RESIDUAL (arm64 FMA drift): %d/%d packets differ for %s[%s] — "+
						"root cause: CELT float FMA contraction vs clang -ffp-contract=on "+
						"(project_arm64_celt_1ulp_drift.md); amd64 asm/CI gate holds",
						len(diffFrames), len(libPackets), tc.name, variant)
					return
				}

				// amd64 pure-Go build: this fixture is the SIMD libopus tier, which is
				// NOT the pure-Go path's matched reference (the build-config-matrix
				// gate links the SCALAR libopus). The Go amd64 float backend tracks
				// the scalar numerics, not the SIMD libopus that produced the fixture,
				// so it diverges from this fixture at a higher rate than the live
				// SCALAR oracle does. The authoritative byte-exact gate for the
				// pure-Go CELT short-frame path is the live-scalar-oracle
				// TestEncoderCBRByteParity{CELT,Summary} (same configs), which holds
				// SILK byte-exact and bounds the CELT/Hybrid near-tie residual; this
				// fixture comparison is logged here. The packet-count structural check
				// above stays hard on every build. See project_arm64_celt_1ulp_drift.md.
				t.Logf("RESIDUAL (amd64-purego vs SIMD fixture, wrong tier): %d/%d packets differ for %s[%s] — "+
					"Go amd64 float tracks the scalar libopus, not the SIMD fixture; the live-scalar "+
					"TestEncoderCBRByteParityCELT gate is authoritative (project_arm64_celt_1ulp_drift.md)",
					len(diffFrames), len(libPackets), tc.name, variant)
			})
		}
	}
}
