// CELT short-frame (2.5 ms / 5 ms) variant byte-parity test.
//
// This file covers the variant-byte ratchet surface for the 2.5 ms and 5 ms
// CELT FB frame sizes. It asserts
// byte-identical packets between gopus and the pinned libopus 1.6.1 variant
// fixture across all four signal variants × both channel counts.
//
// Architecture policy (same as all CELT byte-parity tests in this package):
//   - amd64: hard byte-equality gate — CI must be byte-exact.
//   - arm64: CELT float FMA contraction differs from clang -ffp-contract=on by
//     ≤1 ULP per operation; diffs are reported as an honest residual, not masked.
//     Root cause: documented in project_arm64_celt_1ulp_drift.md.
//
// Reference: libopus src/opus_encoder.c opus_encode_native()
// fixture: testdata/encoder_compliance_libopus_variants_fixture.json
package testvectors

import (
	"bytes"
	"fmt"
	"runtime"
	"testing"

	"github.com/thesyncim/gopus/internal/encoder"
	"github.com/thesyncim/gopus/internal/testsignal"
	"github.com/thesyncim/gopus/types"
)

